void main() {
  int nilai_branching = 55;  

  // Tugas Percabangan (Branching)
  if (nilai_branching > 70) {
    print("Nilai A");
  } else if (nilai_branching > 40){
    print("Nilai B");
  }else if(nilai_branching > 0){
    print("Nilai C");
  }else{
    return null;
  }

  nilai_branching;

  // Tugas Perulangan (Looping)
  for (var i = 1; i < 11; i++) {
    print(i);
  }

  
}
